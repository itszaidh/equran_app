def update_arb_file(lang, new_kv):
    file_path = os.path.join(L10N_DIR, f"app_{lang}.arb")
    if not os.path.exists(file_path):
        print(f"ARB file not found: {file_path}")
        return

    with open(file_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    # Merge new key-values (preserving order by deleting old and appending if they existed)
    for k, v in new_kv.items():
        if k in data:
            del data[k]
        data[k] = v

    with open(file_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f"Updated {file_path}")

if __name__ == "__main__":
    for lang, kv in TRANSLATIONS.items():
        update_arb_file(lang, kv)
